<p class="lead">Verdi NTNU er en studentforening for finansinteresserte studenter ved NTNU. Gruppen består av omkring 25 aktive medlemmer fra ulike årstrinn og studier på NTNU. </p>
<h3>Hva gjør vi i Verdi? </h3>
<p class="lead">Vi har medlemsmøter én dag i uken, hvor vi blant annet diskuterer finansnyheter og finansteori, og det presenteres analyser av aksjer og andre investeringsmuligheter. I tillegg til det ukentlige møtet arbeides det i grupper på 4-6 personer med analyse av mulige investeringer, som presenteres på Verdi-møtene. Det fokuseres på å sette sammen grupper med studenter fra ulike årstrinn og med ulik erfaring innen finans, slik at det sikres at alle grupper har gode forutsetninger for å kunne gjennomføre analyser. Erfaringer fra tidligere år, er at gruppearbeidet er en veldig god læringsarena for nye medlemmer med mindre eller ingen erfaring med aksjer eller andre investeringer. </p>
<p class="lead">Aksjeanalysene danner grunnlaget for forvaltning av et veldedighetsfond, hvor deler av overskuddet går til veldedige formål. Verdi deltar også i Nordnet sin skolekonkurranse (aksjeinvestering), som har vært arrangert de siste semestrene. </p>
<p class="lead">Et annet viktig fokus for Verdi for å skape finansinteresse på NTNU, er å arrangere foredrag med kjente finanspersonligheter. Hvert semester arrangerer vi 2-3 foredrag. Som medlem i Verdi vil du kunne være med i arbeidet med å skaffe de foredragsholderne du har størst ønske om å få til Trondheim.</p>
<p class="lead">Det er et veldig godt sosialt samhold i Verdi, og vi har mange sosiale aktiviteter utover de vanlige medlemsmøtene. Vi samles gjerne på kvelden etter møtene, har julebord, reiser på hyttetur og har grillfest på våren, for å nevne noe.
</p>
<h3>Hvem kan søke Verdi?</h3>
<p class="lead">Det er ikke noe krav til at du skal ha erfaring med aksjer eller annen bakgrunn innenfor finans. I starten av hvert semester legger vi opp til opplæring av nye medlemmer, og gruppearbeidet (som beskrevet over) er en kontinuerlig læringsarena. Det viktigste er at du har interesse for finans, og et ønske om å lære mer innenfor dette området.
<p class="lead"><b>Vi søker studenter fra alle årstrinn og fra alle linjer på NTNU. Dersom du har lyst til å lære mer om finans, analysere aksjer, arrangere foredrag med ettertraktede foredragsholdere eller være med å utvikle Verdi NTNU sin hjemmeside er du av stor interesse for Verdi NTNU.</b>

<h4>Årets søknadsfrist har nå gått ut, men vi oppfordrer alle til å søke opptak høsten 2016. </h4>

